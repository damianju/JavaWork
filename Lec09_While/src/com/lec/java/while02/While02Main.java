package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");

		// 구구단 2ㄷ단
		// while문 사용
		// 구구단 2단을 while문을 사용해서 출력

		int i = 1;
		while (i <= 9) {
			System.out.println("2x" + i + "=" + 2 * i);
			i++;
		} // end while

	} // end main()

} // end class While02Main
